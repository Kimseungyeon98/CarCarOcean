package kr.checker.vo;

public class CheckerVo {
	private int checker_num;
	private String checker_name;
	private String checker_photo;
	private String checker_company;
	private String checker_phone;
	public int getChecker_num() {
		return checker_num;
	}
	public void setChecker_num(int checker_num) {
		this.checker_num = checker_num;
	}
	public String getChecker_name() {
		return checker_name;
	}
	public void setChecker_name(String checker_name) {
		this.checker_name = checker_name;
	}
	public String getChecker_photo() {
		return checker_photo;
	}
	public void setChecker_photo(String checker_photo) {
		this.checker_photo = checker_photo;
	}
	public String getChecker_company() {
		return checker_company;
	}
	public void setChecker_company(String checker_comany) {
		this.checker_company = checker_comany;
	}
	public String getChecker_phone() {
		return checker_phone;
	}
	public void setChecker_phone(String checker_phone) {
		this.checker_phone = checker_phone;
	}
}
