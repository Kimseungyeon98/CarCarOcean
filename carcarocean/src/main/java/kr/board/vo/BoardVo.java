package kr.board.vo;

public class BoardVo {
	private int board_num;
	private int mem_num;
	private String board_title;
	private String board_content;
	private String photo;
	private int board_hit;
	private int board_report;
	private String board_reg;
	private String board_modify;
	
	public int getBoard_num() {
		return board_num;
	}
	public void setBoard_num(int board_num) {
		this.board_num = board_num;
	}
	public int getMem_num() {
		return mem_num;
	}
	public void setMem_num(int mem_num) {
		this.mem_num = mem_num;
	}
	public String getBoard_title() {
		return board_title;
	}
	public void setBoard_title(String board_title) {
		this.board_title = board_title;
	}
	public String getBoard_content() {
		return board_content;
	}
	public void setBoard_content(String board_content) {
		this.board_content = board_content;
	}
	public String getPhoto() {
		return photo;
	}
	public void setPhoto(String photo) {
		this.photo = photo;
	}
	public int getBoard_hit() {
		return board_hit;
	}
	public void setBoard_hit(int board_hit) {
		this.board_hit = board_hit;
	}
	public int getBoard_report() {
		return board_report;
	}
	public void setBoard_report(int board_report) {
		this.board_report = board_report;
	}
	public String getBoard_reg() {
		return board_reg;
	}
	public void setBoard_reg(String board_reg) {
		this.board_reg = board_reg;
	}
	public String getBoard_modify() {
		return board_modify;
	}
	public void setBoard_modify(String board_modify) {
		this.board_modify = board_modify;
	}
	
	
}
