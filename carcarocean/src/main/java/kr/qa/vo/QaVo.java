package kr.qa.vo;

public class QaVo {
	private int qa_num;
	private int mem_num;
	private String qa_title;
	private String qa_content;
	private String qa_photo;
	private int qa_status;
	private String qa_reg;
	private String qa_modify;
	
	private String mem_id;			
	private String photo;
	
	public int getQa_num() {
		return qa_num;
	}
	public void setQa_num(int qa_num) {
		this.qa_num = qa_num;
	}
	public int getMem_num() {
		return mem_num;
	}
	public void setMem_num(int mem_num) {
		this.mem_num = mem_num;
	}
	public String getQa_title() {
		return qa_title;
	}
	public void setQa_title(String qa_title) {
		this.qa_title = qa_title;
	}
	public String getQa_content() {
		return qa_content;
	}
	public void setQa_content(String qa_content) {
		this.qa_content = qa_content;
	}
	public String getQa_photo() {
		return qa_photo;
	}
	public void setQa_photo(String qa_photo) {
		this.qa_photo = qa_photo;
	}
	public int getQa_status() {
		return qa_status;
	}
	public void setQa_status(int qa_status) {
		this.qa_status = qa_status;
	}
	public String getQa_reg() {
		return qa_reg;
	}
	public void setQa_reg(String qa_reg) {
		this.qa_reg = qa_reg;
	}
	public String getQa_modify() {
		return qa_modify;
	}
	public void setQa_modify(String qa_modify) {
		this.qa_modify = qa_modify;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getPhoto() {
		return photo;
	}
	public void setPhoto(String photo) {
		this.photo = photo;
	}
	
}
