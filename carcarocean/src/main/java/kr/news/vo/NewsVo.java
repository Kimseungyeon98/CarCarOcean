package kr.news.vo;

public class NewsVo {
	private int news_num;
	private String news_title;
	private String news_content;
	private String news_photo;
	private int news_hit;
	private String news_reg;
	private String news_modify;
	
	public int getNews_num() {
		return news_num;
	}
	public void setNews_num(int news_num) {
		this.news_num = news_num;
	}
	public String getNews_title() {
		return news_title;
	}
	public void setNews_title(String news_title) {
		this.news_title = news_title;
	}
	public String getNews_content() {
		return news_content;
	}
	public void setNews_content(String news_content) {
		this.news_content = news_content;
	}
	public String getNews_photo() {
		return news_photo;
	}
	public void setNews_photo(String news_photo) {
		this.news_photo = news_photo;
	}
	public int getNews_hit() {
		return news_hit;
	}
	public void setNews_hit(int news_hit) {
		this.news_hit = news_hit;
	}
	public String getNews_reg() {
		return news_reg;
	}
	public void setNews_reg(String news_reg) {
		this.news_reg = news_reg;
	}
	public String getNews_modify() {
		return news_modify;
	}
	public void setNews_modify(String news_modify) {
		this.news_modify = news_modify;
	}
		
}
